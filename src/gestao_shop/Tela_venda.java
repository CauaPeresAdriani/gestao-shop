private void atualizarLista() throws ClassNotFoundException {
    DefaultListModel<String> model = new DefaultListModel<>();
    for (Venda v : itensVenda) {
        String nomeProduto = "";
        try {
            nomeProduto = banco.buscarNomeProdutoPorId(v.getIdProduto());
        } catch (SQLException e) {
            nomeProduto = "Nome não encontrado";
        }
        
        model.addElement("Produto: " + nomeProduto +
                         " (ID: " + v.getIdProduto() + ")" +
                         ", Quantidade: " + v.getQuantidade() + 
                         ", Total: R$ " + String.format("%.2f", v.getValorTotal()));
    }
    jList1.setModel(model);
}

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   try {
        quantidade = Double.parseDouble(quantidadeTextField.getText());       
        idProduto = Integer.parseInt(idProdutoTextField.getText());

        double preco = banco.buscarPrecoPorId(idProduto);
        double valorTotal = preco * quantidade;

        jLabel5.setText("Valor Total: R$ " + String.format("%.2f", valorTotal));

        Venda item = new Venda();
        item.setIdProduto(idProduto);
        item.setQuantidade(quantidade);
        item.setValorTotal(valorTotal);
        item.setData(LocalDate.now());

        itensVenda.add(item);

        atualizarLista();
    } catch (SQLException e) {
        jLabel5.setText("Erro ao buscar preço do produto. " + e.getMessage());
    } catch (NumberFormatException ex) {
        jLabel5.setText("Quantidade ou ID do produto inválido.");
    }   catch (ClassNotFoundException ex) {
            Logger.getLogger(Tela_venda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

