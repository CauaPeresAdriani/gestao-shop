    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
        for (Venda item : itensVenda) {
           
            item.setIdUsuario(usuario.getId());  
            
          
            banco.registrarVenda(item);
        }

       
        itensVenda.clear();
        atualizarLista();
        jLabel5.setText("Venda registrada com sucesso!");
        
    } catch (ClassNotFoundException | SQLException e) {
        jLabel5.setText("Erro ao registrar venda: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void BotaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoRemoverActionPerformed
         int selectedIndex = jList1.getSelectedIndex();  // índice do item selecionado

    if (selectedIndex != -1) {
        itensVenda.remove(selectedIndex);  
             try {
                 atualizarLista();  
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(Tela_venda.class.getName()).log(Level.SEVERE, null, ex);
             }
        jLabel5.setText("Item removido da venda.");
    } else {
        jLabel5.setText("Selecione um item para remover.");
    }
    }//GEN-LAST:event_BotaoRemoverActionPerformed

